import pandas as pd

# What is Pandas?
# Pandas is a Python library used for data manipulation and analysis.

# It provides two main data structures:

# Series → One-dimensional (like a column in Excel or SQL).
# DataFrame → Two-dimensional (like an Excel sheet or SQL table).
# It is built on top of NumPy and works well with other libraries like Matplotlib, Seaborn, and Scikit-learn.


# Why Use Pandas?
# Handle structured data (tables, CSV, Excel, SQL, JSON).
# Clean, transform, and prepare data for analysis.
# Perform fast aggregations and computations.
# Easy integration with data visualization and machine learning.

